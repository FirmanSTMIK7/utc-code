#include <iostream>
using namespace std;

int main(){
    int a, b;
    
    cout << "nilai a : ";
    cin >> a;
    cout << "nilai b : ";
    cin >> b;
    cout << "nilai a sama dengan b = " << (a == b) << endl;
    cout << "nilai a lebih kecil dari b = " << (a <= b) << endl;
    cout << "nilai a lebih besar dari b = " << (a >= b) << endl;
    cout << "nilai tidak sama dengan b = " << (a != b) << endl;
   
}